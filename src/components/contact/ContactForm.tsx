import { useState } from "react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Textarea } from "@/components/ui/textarea";
import { useToast } from "@/components/ui/use-toast";
import { supabase } from "@/integrations/supabase/client";
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from "@/components/ui/select";

const ContactForm = () => {
  const { toast } = useToast();
  const [isSubmitting, setIsSubmitting] = useState(false);

  const handleSubmit = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    
    // Get the honeypot field value
    const honeypotValue = (e.target as HTMLFormElement).website.value;
    
    // If honeypot is filled, silently reject the submission
    if (honeypotValue) {
      console.log("Potential spam detected");
      // Still show success message to avoid giving feedback to bots
      toast({
        title: "Bericht verzonden",
        description: "We nemen zo spoedig mogelijk contact met u op.",
      });
      return;
    }
    
    setIsSubmitting(true);

    try {
      const formData = {
        firstName: (e.target as HTMLFormElement).firstName.value,
        lastName: (e.target as HTMLFormElement).lastName.value,
        email: (e.target as HTMLFormElement).email.value,
        phone: (e.target as HTMLFormElement).phone.value,
        topic: (e.target as HTMLFormElement).topic.value,
        message: (e.target as HTMLFormElement).message.value,
      };

      const { error } = await supabase.functions.invoke('send-contact-email', {
        body: formData,
      });

      if (error) throw error;

      toast({
        title: "Bericht verzonden",
        description: "We nemen zo spoedig mogelijk contact met u op.",
      });
      
      (e.target as HTMLFormElement).reset();
    } catch (error) {
      console.error('Error sending message:', error);
      toast({
        title: "Er is iets misgegaan",
        description: "Probeer het later opnieuw of neem telefonisch contact op.",
        variant: "destructive",
      });
    } finally {
      setIsSubmitting(false);
    }
  };

  return (
    <form onSubmit={handleSubmit} className="space-y-6">
      {/* Honeypot field - hidden from users but visible to bots */}
      <div className="hidden" aria-hidden="true">
        <label htmlFor="website">Website</label>
        <input
          type="text"
          id="website"
          name="website"
          tabIndex={-1}
          autoComplete="off"
        />
      </div>

      <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
        <div className="space-y-2">
          <label htmlFor="firstName" className="text-sm font-medium">
            Voornaam
          </label>
          <Input
            id="firstName"
            name="firstName"
            required
            className="w-full"
          />
        </div>
        <div className="space-y-2">
          <label htmlFor="lastName" className="text-sm font-medium">
            Achternaam
          </label>
          <Input
            id="lastName"
            name="lastName"
            required
            className="w-full"
          />
        </div>
      </div>

      <div className="space-y-2">
        <label htmlFor="email" className="text-sm font-medium">
          E-mail
        </label>
        <Input
          id="email"
          name="email"
          type="email"
          required
          className="w-full"
        />
      </div>

      <div className="space-y-2">
        <label htmlFor="phone" className="text-sm font-medium">
          Telefoonnummer
        </label>
        <Input
          id="phone"
          name="phone"
          type="tel"
          className="w-full"
        />
      </div>

      <div className="space-y-2">
        <label htmlFor="topic" className="text-sm font-medium">
          Onderwerp
        </label>
        <Select name="topic" required>
          <SelectTrigger className="w-full">
            <SelectValue placeholder="Selecteer een onderwerp" />
          </SelectTrigger>
          <SelectContent>
            <SelectItem value="offerte">Offerte aanvragen</SelectItem>
            <SelectItem value="informatie">Informatie over tenten</SelectItem>
            <SelectItem value="beschikbaarheid">Beschikbaarheid checken</SelectItem>
            <SelectItem value="samenwerking">Zakelijke samenwerking</SelectItem>
            <SelectItem value="anders">Anders</SelectItem>
          </SelectContent>
        </Select>
      </div>

      <div className="space-y-2">
        <label htmlFor="message" className="text-sm font-medium">
          Bericht
        </label>
        <Textarea
          id="message"
          name="message"
          required
          className="min-h-[150px] w-full"
        />
      </div>

      <Button type="submit" className="w-full" disabled={isSubmitting}>
        {isSubmitting ? "Verzenden..." : "Verstuur Bericht"}
      </Button>
    </form>
  );
};

export default ContactForm;